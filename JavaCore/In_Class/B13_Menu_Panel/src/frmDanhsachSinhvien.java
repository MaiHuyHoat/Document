    public void HienThiDSSV(){
        String tukhoa=this.txtTimKiem.getText();
        Vector<clsSinhvien> danhsach=new Vector();
        int ketqua= CSDL.tbSinhvien.TimSinhvien(tukhoa, danhsach);
        if(ketqua==-2){
            JOptionPane.showMessageDialog(this,"Lỗi truy vấn SQL");
        }
        else if(ketqua==-1){
            JOptionPane.showMessageDialog(this, "Lỗi kết nối CSDL.");
        }
        else if(ketqua==0){
            JOptionPane.showMessageDialog(this, "Không có bản ghi nào.");
        }
        else {
            DefaultTableModel dtm=(DefaultTableModel)this.tblDSSV.getModel();
            dtm.setRowCount(0);
           for(clsSinhvien element:danhsach){
               String masv=element.getMasv();
               String hoten=element.getHoten();
               String dienthoai=element.getDienthoai();
               dtm.addRow(new Object[] {masv,hoten,dienthoai});
           }
        }
                
    }
    
