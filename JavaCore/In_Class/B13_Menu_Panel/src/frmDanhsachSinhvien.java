    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String masv=this.txtMaSV.getText();
        String hoten=this.txtHovaTen.getText();
        String dienthoai=this.txtDienThoai.getText();
        Connection cnn=CSDL.Database.ketnoiMySql();
        if(cnn==null){
            JOptionPane.showMessageDialog(this,"Lỗi kết nối.");
        }else{
            try {
                String sql="INSERT INTO tbsinhvien(masv,hoten,dienthoai) VALUES (?,?,?)";
                PreparedStatement pst=cnn.prepareStatement(sql);
                pst.setString(1,masv);
                pst.setString(2,hoten);
                pst.setString(3,dienthoai);
                int u=pst.executeUpdate();
              if(u>0){
                  JOptionPane.showMessageDialog(this,"Thêm sinh viên thành công");
                  this.HienThiDSSV();
                  this.xoaO();
              }
            
            } catch (SQLException ex) {
                Logger.getLogger(frmDanhsachSinhvien.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this,"Lỗi truy vấn SQL");
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

